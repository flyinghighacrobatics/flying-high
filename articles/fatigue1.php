<H2><A NAME="SECTION00010000000000000000">
Introduction</A>
</H2>

<P>
Fatigue is an inevitable part of exercising, and fundamental to create the necessary training stimulus for increases in performance and  adaptations to occur,  but with the presence of fatigue comes increased injury risk <A NAME="tex2html16" target="contents"
  HREF="#Woods2003">Woods et&nbsp;al. (2003)</A>; <A NAME="tex2html17" target="contents"
  HREF="#McLean2009">McLean &amp; Samorezov (2009)</A>. In acrobatic sports the frequency of injury and the presence of pain is a major limiting factor to any long term training and performance goal  <A
 HREF="#Purnell2010">Purnell et&nbsp;al. (2010)</A>. However the current consensus in the field of injury prevention is that most of the processes related to fatigue, that are suspected in leading to injury and pain, are trainable <A NAME="tex2html18" target="contents"
  HREF="#Schiftan2014">Schiftan et&nbsp;al. (2014)</A>; <A NAME="tex2html19" target="contents"
  HREF="#Woods2003">Woods et&nbsp;al. (2003)</A>; <A NAME="tex2html20" target="contents"
  HREF="#McLean2009">McLean &amp; Samorezov (2009)</A>; <A NAME="tex2html21" target="contents"
  HREF="#Greig2007">Greig &amp; Walker-Johnson (2007)</A>.
<BR>This article briefly introduces the concept of fatigue and how it is related to injury, it gives a quick look at the injury rates in acrobatics, and the specific adaptations of the nervous system with certain types of training, that seem to be key in promoting stability and preventing injury. Namely the effects of strength and balance training on the nervous system, and how these effects functionally can lead to improved injury prevention in training planning.
<BR>&nbsp;
<BR>
<P>

<H2><A NAME="SECTION00020000000000000000">
Fatigue</A>
</H2>
Muscle fatigue is an exercise-induced reduction in maximal voluntary muscle force. It may arise not only because of peripheral changes at the level of the muscle (peripheral fatigue), but also because the central nervous system fails to drive the moto-neurons adequately (central fatigue) (<A
 HREF="#Mcardle2010">McArdle et&nbsp;al., 2010</A>, p. 393).
<BR>Several studies have provided evidence for correlations between the risk of specific sport injuries, and the effects of  fatigue <A NAME="tex2html22" target="contents"
  HREF="#McLean2009">McLean &amp; Samorezov (2009)</A>; <A NAME="tex2html23" target="contents"
  HREF="#Schiftan2014">Schiftan et&nbsp;al. (2014)</A>; <A NAME="tex2html24" target="contents"
  HREF="#Woods2003">Woods et&nbsp;al. (2003)</A>. For example muscular fatigue has been related to the incidence of injuries in soccer, as most injuries are observed during the last third of each half of matches <A
 HREF="#Woods2003">Woods et&nbsp;al. (2003)</A>.
<BR>
<P>
Fatigue is inherently a part of exercising and it's not that we want to avoid becoming fatigued, however it is worth taking into account in our training planning, and preparation that there are imbalanced patterns at the core of injury causality, and that these compensations can be identified, and prevented through targeted training. In acrobatic sports there is a prevalence of shoulder issues, carrying your bodyweight overhead in a handstand requires a high degree of overhead shoulder stability and a proper balance in the engagement of the shoulder muscles. In overhead athletes, like the volley ball player hitting a ball or the acrobat performing a handstand, the activation pattern of the shoulder muscles play a key role in how the different structures of the shoulder joints are loaded. If the athlete has imbalances in this activation pattern, like certain muscles being over activated and thus overpowering other muscles, there is a risk of placing high loads on the shoulder joint in a unstable or un-advantageous position, ultimately leading to pain and injury.
<BR> For example a recent study into how Glenohumeral external rotation muscle fatigue might contribute to shoulder injuries in overhead athletes, highlights the interdependence of scapular and glenohumeral force couples, in other words the importance of a proper balance between the muscles that act on the shoulder. Fatigue-induced alterations in the lower trapezius (Trap-3) might predispose the infraspinatus to injury through chronically increased activation <A
 HREF="#pmid21944066">Joshi et&nbsp;al. (2011)</A>.
Targeted training of the lower trapezius and the external rotator muscles of the shoulder is thus a highly recommended injury prevention strategy for overhead athletes, and will help the acrobat a lot in stabilizing the shoulder joint when performing the handstand.
<BR>&nbsp;
<BR>
<P>

<H2><A NAME="SECTION00030000000000000000">
Fatigue &amp; Injury</A>
</H2>
Injury causality and prevention is a very complex field, often conflicting evidence arises for the assumption, that fatigue induced alterations in activation pattern are causing instability of a joint. For example there is a specific recruitment sequence in the shoulder muscles in response to a sudden arm movement, and one study concluded that this muscle activation pattern is delayed but not altered with fatigue <A
 HREF="#Cools2002">Cools et&nbsp;al. (2002)</A> and another study by <A
 HREF="#pmid21944066">Joshi et&nbsp;al.</A> concluded that fatigue-induced alterations in the lower trapezius might predispose the infraspinatus to injury through chronically increased activation. Despite the conflicting evidence in regards to the effect of fatigue on muscle activation pattern, most studies agree that there are correlations between fatigue of central control processes, and injury risk.
<BR>Looking at the effects of fatigue on stability, it seems that even though balance performance is maintained throughout the exercise, a change in balance strategy is evident during the latter stages, with a shift toward anterior displacement, such compensatory strategies might increase the risk of injury to both muscular and ligamentous structures, particularly in more dynamic movements <A
 HREF="#Greig2007">Greig &amp; Walker-Johnson (2007)</A>. Furthermore fatigue of one leg induces a fatigue crossover to the other leg during single-leg landings. Central fatigue thus seems to be a critical component of fatigue-induced sports landing strategies. Hence, targeted training of central control processes may be necessary to  successfully counter the debilitative impact of fatigue on specific injury risk <A
 HREF="#McLean2009">McLean &amp; Samorezov (2009)</A>.
<BR>
<P>
Besides shoulder injuries and ankle sprains, low back pain is a prevalent issue for many acrobats <A
 HREF="#Purnell2010">Purnell et&nbsp;al. (2010)</A>, and a direct relationship between low back pain and neuromuscular imbalance has been documented, in athletes with low back pain <A
 HREF="#Renkawitz2006">Renkawitz et&nbsp;al. (2006)</A>. In patients with chronic low back pain, muscle recruitment was also altered with the presence of muscle fatigue <A
 HREF="#Abboud2014">Abboud et&nbsp;al. (2014)</A>. Interesting to note here is, that even though strength training has shown to be an effective treatment modality for low back pain, Maximum isometric trunk extension strength had no relationship to the presence of Low Back Pain or the occurrence of neuromuscular imbalance of erector spinae <A
 HREF="#Renkawitz2006">Renkawitz et&nbsp;al. (2006)</A>. A study by <A
 HREF="#Hashemirad2009">Hashemirad et&nbsp;al. (2009)</A> indicates that flexibility plays an important role in trunk muscular recruitment pattern and the strategy of the central nervous system to provide stability.
 What to take from this is that even though we do not fully understand the processes of fatigue that result in injury, one evident principle is that effective injury prevention involves proper neuromuscular balance, targeted training of central control processes and range of motion.
<BR>&nbsp;
<BR>
<H2><A NAME="SECTION00040000000000000000">
Injury rates</A>
</H2>
If we look  at the stats, not that many studies have focused on partner acrobatics, but it won't come as a surprise that the floor can be dangerous, the floor has been identified as the most hazardous of all the apparatus on which gymnasts perform. Acrobatic routines involve similar skills to gymnastic floor routines, with the additional physical stresses of partner balances and throws, and we see injury rates of 2.94 per 1000 h of participation, or 1.21 injuries per acrobat per year, which is slightly lower than artistic gymnastics but higher than cheerleading.  The higher injury rate in acrobatics compared with cheerleading is expected as the ratio of
bases to tops is often greater in cheerleading group skills, hence the
load of the top is spread amongst more bases. <A
 HREF="#Purnell2010">Purnell et&nbsp;al. (2010)</A>.
<BR>&nbsp;
<BR>
<P>

<H2><A NAME="SECTION00050000000000000000">
Training specific reflex adaptations</A>
</H2>
When an injury results in acute pain, the nervous system undergoes an adaptive neuroplastic response resulting in an increase in sensitivity. After some time, the pain neuroplasticity is normalized as the injury heals. In some cases, such neuroplastic processes fail to normalize, and acute pain develops into chronic pain with hypersensitivity. The continuing pain after injury resolution may be due to maladaptive pain neuroplasticity. In contrast, advantageous neuroplasticity permits the nervous system to cope with challenges such as maladaptive pain neuroplasticity to help returning to a pain-free state <A
 HREF="#Navarro2009">Navarro (2009)</A>.
<BR>
<P>
Spinal reflexes where previously viewed as stereotyped motor patterns with limitid scope for modification. However, evidence suggests that even short-latency, largely monosynaptic reflexes show a high degree of modulation during simple human motor activities such as walking and standing, and that the pattern of modulation can be specifically altered for the different functional requirements of each activity <A
 HREF="#Capaday1988">Stein &amp; Capaday (1988)</A>. This is further supported by the evidence for adaptations in reflex excitability that are specific with respect to the type of training. For example <A
 HREF="#Trimble1994">Trimble &amp; Koceja (1994)</A> provided evidence for reflex down-training in man during balance training. This Reduced reflex excitability have been demonstrated for both power- and balance-trained athletes <A
 HREF="#Gruber2007">Gruber et&nbsp;al. (2007)</A>.
<BR>&nbsp;
<BR>
<P>

<H2><A NAME="SECTION00060000000000000000">
The effects of strength and balance training</A>
</H2>
   Some controversy remain but several studies have provided evidence, that balance and strength training induce changes in short latency reflexes and that these reflex adaptation forms part of the neuroplasticity induced by specific training <A NAME="tex2html25" target="contents"
  HREF="#Paavo2003">Komi (2003)</A>; <A NAME="tex2html26" target="contents"
  HREF="#Gruber2007">Gruber et&nbsp;al. (2007)</A>.    A reduction in reflexes could be associated with either neural adaptations or a shift in muscle fiber distribution after a sustained Strength training over several months <A
 HREF="#Gruber2007">Gruber et&nbsp;al. (2007)</A>. Functionally this reduction in reflex excitability could  enable more cocontractions of muscles associated with postural controle, thus creating better stability. Neuromuscular control and Proprioception the sensory feedback from muscles and joints about position, length, etc are fundamental factors in preventing injury.  It is  therefore recommended that athletes  perform proprioception (balance) and neuromuscular balance (strength)  training in the rested and exercised state to further develop neuromuscular control <A NAME="tex2html27" target="contents"
  HREF="#Schiftan2014">Schiftan et&nbsp;al. (2014)</A>; <A NAME="tex2html28" target="contents"
  HREF="#Greig2007">Greig &amp; Walker-Johnson (2007)</A>.
<BR>
<P>
While we are learning and as we progress in our practice, we intuitively know that the more hours we rack up in training the higher our probability of sustaining an injury becomes. Statistics dictate that with increased training load comes increased injury risk, either from a fall (sudden onset injury) or from inadvertently overloading soft tissues and joints during practice (overload /specialization injury). Risk is part of what makes acrobatics fun, we embrace the risk of getting hurt that is inherent to playing, but just as we use spotters to minimize the risk of falling, we want to condition ourselves in a way that minimizes the risk of overload or specialization injuries.
<BR>
<P>
The causality patterns of most sport injuries are multi factorial in nature,  but freak occurrences and shear bad luck a side, fatigue  related mechanism like alterations in muscle activation pattern, or reinforced compensations due to neuromuscular imbalances, are suspected in leading to adverse loads being placed on different structures of the body ultimately leading to pain or injury <A NAME="tex2html29" target="contents"
  HREF="#McLean2009">McLean &amp; Samorezov (2009)</A>; <A NAME="tex2html30" target="contents"
  HREF="#Meeuwisse2007">Meeuwisse et&nbsp;al. (2007)</A>.
With these patterns being trainable through strength and balance training, it is evident that a little effort in targeted training can lead to improved injury prevention in acrobatic training.
<BR>&nbsp;
<BR>
<P>

<H2><A NAME="SECTION00070000000000000000">
Conclusion &amp; Application in training planning</A>
</H2>

<P>
In this article we have looked at some of the correlations between fatigue and sports injury, one evident principle is that neuromuscular balance and control, are fundamental factors in preventing injury. Stability and neuromuscular control involves reflex adaptations, specifically reduced reflex excitability, that functionally allows for more co-contraction  of muscles associated with postural control. With reduced reflex excitability having been demonstrated for both power- and balance-trained athletes <A
 HREF="#Gruber2007">Gruber et&nbsp;al. (2007)</A>, strength and balance training are key elements of promoting stability and neuromuscular control.  The recommendation for acrobats to train for injury prevention is targeted training of the lower trapezius and the external rotator muscles of the shoulder, plus targeted training of proper neuromuscular balance of the low back/ hip area. Because of the similar reflex adaptations with balance and strength training, a fundamental element in preventing injury and improving performance in acrobatics, is thus strength training focused on proper neuromuscular balance.

